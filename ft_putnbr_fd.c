#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int i;

	i = 1;
	if (n == -2147483648)
	{
		ft_putstr_fd("-2147483648", fd);
		return ;
	}
	if (n < 0)
	{
		ft_putchar_fd('-', fd);
		n = n * -1;
	}
	while (n / i >= 10)
	{
		i = i * 10;
	}
	while (i > 0)
	{
		ft_putchar_fd((n / i) % 10 + '0', fd);
		i = i / 10;
	}
}
